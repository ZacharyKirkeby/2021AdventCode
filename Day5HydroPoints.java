package days;
public class Day5HydroPoints {
		private int x1;
		  private int y1;
		  private int x2;
		  private int y2;


		  public Day5HydroPoints(int x, int y,int xx, int yy){
		    x1 = x;
		    y1 = y;
		    x2 = xx;
		    y2 = yy;

		  }

		  public int getX1(){
		    return x1;
		  }

		  public int getY1(){
		    return y1;
		  }

		public int getX2(){
		    return x2;
		  }

		  public int getY2(){
		    return y2;
		  }
		  
		}

